function createTrail(x, y) {
    const dot = document.createElement('div');
    dot.style.cssText = `
        position: fixed;
        left: ${x}px;
        top: ${y}px;
        width: 4px;
        height: 4px;
        background: var(--primary);
        border-radius: 50%;
        pointer-events: none;
        z-index: 9999;
        animation: fadeOut 0.6s ease-out forwards;
    `;
    
    document.body.appendChild(dot);
    
    setTimeout(() => {
        dot.remove();
    }, 600);
}

